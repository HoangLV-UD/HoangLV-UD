    public void fillTbaleDonVi() {
        DefaultTableModel tblModel = (DefaultTableModel) tblDonVi.getModel();
        tblModel.setRowCount(0);
        try {
            List<DonVi> list = dvDao.selectAll();
            for (DonVi dm : list) {
                tblModel.addRow(new Object[]{
                    dm.getMaDon_vi(),
                    dm.getTenDon_vi(),
                    dm.isTrangThai() ? "Hoạt đỘng" : "Tạm dừng"
                });
            }
        } catch (Exception e) {
        }
    }

    public DonVi getform() {
        DonVi dm = new DonVi();
        dm.setMaDon_vi(txtMaDV.getText());
        dm.setTenDon_vi(txtTenDv.getText());
        dm.setTrangThai(jckHD.isSelected());
        return dm;
    }

    public void setForm(DonVi dm) {
        txtMaDV.setText(dm.getMaDon_vi());
        txtTenDv.setText(dm.getTenDon_vi());
        jckHD.setSelected(dm.isTrangThai());
        jckTN.setSelected(!dm.isTrangThai());
    }
    private void insert() {
       try {
            dvDao.insert(getform());
            JOptionPane.showMessageDialog(this, "Thêm mới thành công");
            this.clearForm();
        } catch (Exception e) {
        }
    }

    private void update() {
       try {
            dvDao.update(getform());
            this.clearForm();
            JOptionPane.showMessageDialog(this, "Cập nhật thành công");
            clearForm();
        } catch (Exception e) {
        }
    }

    private void clearForm() {
      DonVi dm = new DonVi();
        this.setForm(dm);
        this.fillTbaleDonVi();
    }

    private void tblDonViMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDonViMouseClicked
        if (evt.getClickCount() == 1) {
            int row = tblDonVi.getSelectedRow();
            String madm = tblDonVi.getValueAt(row, 0).toString();
            DonVi nv = dvDao.selectID(madm);
            this.setForm(nv);
            tblDonVi.setRowSelectionInterval(row, row);
        }
    }//GEN-LAST:event_tblDonViMouseClicked

